// Helper to search songs locally
export const searchLocalSongs = (query: string): Song[] => {
  const q = query.toLowerCase();
  return MOCK_SONGS.filter(
    (song) =>
      song.title.toLowerCase().includes(q) ||
      song.artist.toLowerCase().includes(q) ||
      song.album.toLowerCase().includes(q) ||
      song.genre.toLowerCase().includes(q) ||
      song.moods.some((mood) => mood.toLowerCase().includes(q))
  );
};

// Helper to filter songs by mood tags
export const getSongsByMoods = (tags: string[]): Song[] => {
  if (!tags || tags.length === 0) return MOCK_SONGS;
  const lowercaseTags = tags.map((t) => t.toLowerCase());
  return MOCK_SONGS.filter((song) =>
    song.moods.some((mood) => lowercaseTags.includes(mood.toLowerCase()))
  );
};
